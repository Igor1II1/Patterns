// ========== Задание 2.4 ==========
// Напиши функцию getDayType(day) через switch.
// Принимает название дня недели (строкой), возвращает:
//   "будний", "выходной" или "неизвестный день"
//
// getDayType("понедельник")  // "будний"
// getDayType("суббота")      // "выходной"
// getDayType("воскресенье")  // "выходной"
// getDayType("праздник")     // "неизвестный день"
//
// Раздел учебника: "switch"
//
// Забегая вперёд — функции (глава 4):
// function имяФункции(параметр) {
//   // код
//   return значение;
// }

function getDayType(day) {
  switch (day) {
    case "понедельник":
    case "вторник":
    case "среда":
    case "четверг":
    case "пятница":
      return "будний";
    case "суббота":
    case "воскресенье":
      return "выходной";
    default:
      return "Неизвестный день";
  }
}

console.log(getDayType("понедельник"));
console.log(getDayType("вторник"));
console.log(getDayType("среда"));
console.log(getDayType("четверг"));
console.log(getDayType("пятница"));
console.log(getDayType("суббота"));
console.log(getDayType("воскресенье"));
console.log(getDayType("праздник"));

// ========== Разбор задания ==========
// Что делали:    писали switch для проверки одной переменной на список конкретных значений.
// Почему так:    switch сравнивает переменную с каждым case через === (строгое сравнение).
//                Если нет break/return — код "проваливается" в следующий case.
//                Это можно использовать: case "пн": case "вт": case "ср": — все ведут
//                к одному результату, без дублирования кода.
// Для чего:      switch читается лучше чем цепочка if/else когда проверяешь одну переменную
//                на конкретные значения (роли, дни недели, статусы, команды).
// Главное понять: switch = "одна переменная, много точных совпадений".
//                default = "если ничего не совпало" (аналог else).
//                return внутри case — выходит из функции, break не нужен.
