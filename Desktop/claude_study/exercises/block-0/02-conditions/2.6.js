// ========== Задание 2.6 ==========
// Напиши функцию classify(n) которая классифицирует число:
//
// classify(0)    // "ноль"
// classify(5)    // "положительное нечётное"
// classify(4)    // "положительное чётное"
// classify(-3)   // "отрицательное нечётное"
// classify(-8)   // "отрицательное чётное"
//
// Подсказка: чётность проверяется через остаток от деления (n % 2 === 0)
//
// Раздел учебника: "if / else if / else", "Логические операторы"

function classify(n) {
  if  (n > 0 && n % 2 === 0) {
    return "положительно четное";
  } else if (n === 0) {
    return "ноль";
  } else if (n > 0 && n % 2 !== 0) {
    return "положительное нечетное";
  } else if (n < 0 && n % 2 !== 0) {
    return "отрицательно нечетное";
  } else {
    return "отрицательное четное";
  }
}


console.log(classify(0)); // "ноль"
console.log(classify(5)); // "положительное нечётное"
console.log(classify(4)); // "положительное чётное"
console.log(classify(-3)); // "отрицательное нечётное"
console.log(classify(-8)); // "отрицательное чётное"

