if ("") console.log("b"); // falsy , значение не выведится 
if ([]) console.log("c");// truhy ,  значение выведится 
if ({}) console.log("d"); // truhy ,  значение выведится 
if (null) console.log("e");// falsy , значение не выведится 
if (undefined) console.log("f");// falsy , значение не выведится 
if ("0") console.log("g");// truhy ,  значение выведится 
if (-1) console.log("h");// truhy ,  значение выведится

