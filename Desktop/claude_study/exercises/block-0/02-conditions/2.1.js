// Напиши функцию getTicketPrice(age) которая возвращает цену билета:
//   - до 5 лет — бесплатно (0)
//   - 5-12 лет — 200 руб.
//   - 13-17 лет — 350 руб.
//   - 18-64 лет — 500 руб.
//   - 65+ лет — 250 руб.
//
// Раздел учебника: "if / else if / else"
//
// Забегая вперёд — функции (глава 4):
// function имяФункции(параметр) {
//   // код
//   return значение;   // вернуть результат
// }



function getTicketPrice(age){
    if(age < 5){
        return 0
    }else if( age<=12){
        return 200
    }else if( age<=17){
        return 350
    }else if( age<=64){
        return 500
    }else{
        return 250
    }
}


console.log(getTicketPrice(4))
console.log(getTicketPrice(11))
console.log(getTicketPrice(18))
console.log(getTicketPrice(64))
console.log(getTicketPrice(70))

