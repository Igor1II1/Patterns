// --- Описание ---
// Функция высшего порядка — это функция, которая принимает другую функцию
// как аргумент (или возвращает функцию).
//
// Функцию, которую передают как аргумент, называют CALLBACK (коллбэк).
// Это одна из самых важных идей в JavaScript.
//
// Пример:
//   function greet(name, formatter) {
//     console.log("Привет, " + formatter(name));
//   }
//   greet("игорь", function(s) { return s.toUpperCase(); });
//   // → "Привет, ИГОРЬ"
//
// Здесь greet — функция высшего порядка, а formatter — callback.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию repeat(n, action)
//    - Вызывает action(i) ровно n раз, где i идёт от 0 до n-1
//    - action — это функция (callback), которую передают как аргумент
//    - Функция ничего не возвращает (просто вызывает action)
//
//    repeat(3, console.log)
//    // выведет:
//    // 0
//    // 1
//    // 2
//
// 2. Напиши функцию applyTwice(fn, value)
//    - Применяет функцию fn к value два раза подряд
//    - Сначала result = fn(value), потом fn(result)
//    - Возвращает итоговый результат
//
//    applyTwice(x => x * 2, 5) → 20  (5*2=10, 10*2=20)
//    applyTwice(x => x + 1, 0) → 2   (0+1=1, 1+1=2)
//    applyTwice(x => x * x, 3) → 81  (3*3=9, 9*9=81)
//
// 3. Напиши функцию unless(condition, action)
//    - Если condition === false, вызывает action()
//    - Если condition === true, ничего не делает
//    - Это "обратный if": выполняет действие ЕСЛИ НЕ выполнено условие
//
//    unless(false, () => console.log("выполнено"))  → выведет "выполнено"
//    unless(true, () => console.log("не выполнено")) → ничего не выведет
//
// --- Тесты ---
//
// --- repeat ---
// let result = "";
// repeat(4, function(i) { result = result + i; });
// result → "0123"
//
// let sum = 0;
// repeat(5, function(i) { sum = sum + i; });
// sum → 10  (0+1+2+3+4)
//
// repeat(0, function(i) { console.log(i); });
// // ничего не выведет (0 раз)
//
// --- applyTwice ---
// applyTwice(x => x * 2, 5)   → 20
// applyTwice(x => x + 1, 0)   → 2
// applyTwice(x => x * x, 3)   → 81
// applyTwice(x => x - 5, 100) → 90
//
// --- unless ---
// let called = false;
// unless(false, () => { called = true; });
// called → true
//
// let called2 = false;
// unless(true, () => { called2 = true; });
// called2 → false
//
