 console.log(operate(10, 5, (a, b) => a - b)); 


function applyAndPrint(value, fn){
    console.log(fn(value)) 
};


applyAndPrint(5, n => n * 2);
applyAndPrint("hello", s => s.length);



function transform(value, fn1, fn2) {
    return fn2(fn1(value));
};
console.log(transform(5, n => n + 1, n => n * 2))
console.log(transform("hello", s => s.toUpperCase(), s => s + "!"))

