// ========== РАЗБОР ЗАДАНИЯ ==========
//
// --- Что делали ---
// Часть A: определяли чистые/нечистые функции (5 штук).
// Часть B: переписали 2 нечистые функции в чистые + написали calculateTotal.
//
// --- Пошаговый разбор (что происходит технически) ---
//
// Часть A — анализ:
// add(a, b) → ЧИСТАЯ: одинаковый вход = одинаковый выход, ничего не меняет снаружи
// addToArray(arr, item) → НЕЧИСТАЯ: arr.push(item) мутирует входной массив
// getRandom() → НЕЧИСТАЯ: каждый вызов возвращает разное число (нет детерминизма)
// formatName(name) → ЧИСТАЯ: trim()/toUpperCase() создают новую строку, не меняют вход
// addToTotal(n) → НЕЧИСТАЯ: читает и меняет внешнюю переменную total
//
// Часть B:
//
// addToTotal(n, total) — чистая версия:
// Шаг 1: Вызов addToTotal(100, 50) — n = 100, total = 50 (оба через параметры)
// Шаг 2: total += n → total = 50 + 100 = 150 (локальная переменная-параметр)
// Шаг 3: return 150
// Повторный вызов addToTotal(100, 50) → снова 150 (чистая! тот же вход = тот же выход)
//
// applyDiscount(price, discount) — чистая версия:
// Шаг 1: Вызов applyDiscount(200, 10) — price = 200, discount = 10
// Шаг 2: return 200 - 10 → 190
// Раньше discount была внешней переменной — теперь передаётся как параметр
//
// calculateTotal(price, quantity, taxRate):
// Шаг 1: Вызов calculateTotal(100, 3, 0.2)
// Шаг 2: price * quantity = 100 * 3 = 300 (базовая стоимость)
// Шаг 3: price * quantity * taxRate = 300 * 0.2 = 60 (налог)
// Шаг 4: 300 + 60 = 360
// Шаг 5: return 360
//
// --- Ошибки при решении ---
// Ошибка 1: addToArray назвал чистой
// Почему неправильно: arr.push(item) МУТИРУЕТ входной массив — это побочный эффект.
//   Если передать [1,2] — после вызова массив станет [1,2,3]. Оригинал изменён.
// Как правильно: чистая версия использовала бы [...arr, item] или arr.concat(item)
// Как мыслить: "Меняется ли что-то, что существовало ДО вызова функции?"
//
// Ошибка 2: getRandom назвал чистой
// Почему неправильно: getRandom() → 0.123, getRandom() → 0.789 — разный выход
//   при одинаковом входе (пустом). Нарушает критерий 1 (детерминизм).
// Как мыслить: "Если вызвать 100 раз с теми же аргументами — всегда один результат?"
//
// --- Почему решение правильное ---
// addToTotal: total передаётся как параметр, не читает внешнюю переменную
// applyDiscount: discount передаётся как параметр, не зависит от внешнего состояния
// calculateTotal: все данные через параметры, чистая математика, результат предсказуем
// Все три функции: одинаковый вход → одинаковый выход + не меняют ничего снаружи
//
// --- Что должен был усвоить ---
// 1. Два критерия чистоты: детерминизм + отсутствие побочных эффектов
// 2. push() мутирует массив — это побочный эффект
// 3. Math.random() нарушает детерминизм
// 4. Чтобы сделать функцию чистой: всё через параметры, результат через return
// 5. Мутация = изменение существующего объекта/массива (не создание нового)
//
// --- Для чего в реальной разработке ---
// Чистые функции: легко тестировать (вход→выход), предсказуемые, нет скрытых зависимостей
// React: компоненты должны быть "чистыми" — одни и те же props = одинаковый рендер
// Redux reducers — обязаны быть чистыми функциями
// Мутация — частый источник багов: "я передал массив, а он изменился"