// --- Описание ---
// Чистая функция: 1) при одинаковых входах — одинаковый выход,
// 2) не меняет ничего снаружи (нет побочных эффектов).
// Нечистая: меняет внешние переменные, массивы, выводит в консоль.
//
// --- Что нужно сделать ---
//
// Шаг A — В ЧАТ:
// Для каждой функции скажи: чистая или нечистая? Почему?
//
// function add(a, b) { return a + b; }
// function addToArray(arr, item) { arr.push(item); return arr; }
// function getRandom() { return Math.random(); }
// function formatName(name) { return name.trim().toUpperCase(); }
// let total = 0;
// function addToTotal(n) { total += n; return total; }
//
// Шаг B — В ФАЙЛ:
//
// --- Как сделать нечистую функцию чистой? ---
//
// Проблема нечистой функции — она МЕНЯЕТ что-то снаружи или ЗАВИСИТ
// от внешнего состояния. Чтобы сделать её чистой:
// → всё что нужно — ПЕРЕДАВАЙ через параметры
// → ничего снаружи НЕ ТРОГАЙ
// → результат ВОЗВРАЩАЙ через return
//
// Пример:
//   // НЕЧИСТАЯ — зависит от внешней переменной tax
//   let tax = 0.2;
//   function getPrice(price) { return price + price * tax; }
//
//   // ЧИСТАЯ — tax передаётся как параметр
//   function getPrice(price, tax) { return price + price * tax; }
//
// Теперь getPrice(100, 0.2) ВСЕГДА вернёт 120. Не зависит ни от чего снаружи.
//
// --- Задания ---
//
// 1. Вот нечистая функция (меняет внешнюю переменную total):
//    let total = 0;
//    function addToTotal(n) { total += n; return total; }
//
//    Перепиши как ЧИСТУЮ: пусть принимает total как параметр
//    и возвращает новое значение, не трогая ничего снаружи.
//
// 2. Вот нечистая функция (зависит от внешней переменной discount):
