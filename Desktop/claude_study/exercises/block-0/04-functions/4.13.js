// --- Описание ---
// Композиция функций — это применение нескольких функций последовательно,
// где результат одной становится входом для следующей.
//
// Пример: если у тебя есть функции add1 и double:
//   function add1(x) { return x + 1; }
//   function double(x) { return x * 2; }
//
//   double(add1(5))  → 12  (сначала 5+1=6, потом 6*2=12)
//
// В этом задании ты напишешь функции, которые делают то же самое,
// но принимают функции как аргументы — это позволяет комбинировать
// ЛЮБЫЕ функции, не зная заранее какие именно.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию chain(value, fn1, fn2, fn3)
//    - Применяет три функции последовательно к значению:
//      сначала fn1(value), потом fn2 к результату, потом fn3
//    - Возвращает итоговый результат: fn3(fn2(fn1(value)))
//
//    chain(5, x => x + 1, x => x * 2, x => x - 3) → 9
//    // Разбор: 5+1=6, 6*2=12, 12-3=9
//
//    chain(10, x => x / 2, x => x + 1, x => x * x) → 36
//    // Разбор: 10/2=5, 5+1=6, 6*6=36
//
// 2. Напиши функцию executeIf(condition, fnTrue, fnFalse, value)
//    - Если condition === true, вызывает fnTrue(value) и возвращает результат
//    - Если condition === false, вызывает fnFalse(value) и возвращает результат
//    - Это как тернарный оператор, но с функциями
//
//    executeIf(true,  x => x * 2, x => x + 10, 5) → 10
//    executeIf(false, x => x * 2, x => x + 10, 5) → 15
//
// --- Тесты ---
//
// --- chain ---
// chain(5, x => x + 1, x => x * 2, x => x - 3)     → 9
// chain(10, x => x / 2, x => x + 1, x => x * x)     → 36
// chain(0, x => x + 5, x => x * 3, x => x - 1)      → 14   (0+5=5, 5*3=15, 15-1=14)
// chain(100, x => x / 10, x => x - 5, x => x * 2)   → 10   (100/10=10, 10-5=5, 5*2=10)
// chain("hello", s => s + "!", s => s + "!", s => s + "!") → "hello!!!"
//
// --- executeIf ---
// executeIf(true,  x => x * 2,  x => x + 10, 5)     → 10
// executeIf(false, x => x * 2,  x => x + 10, 5)     → 15
// executeIf(true,  x => x * x,  x => x + 1, 4)      → 16
// executeIf(false, x => x * x,  x => x + 1, 4)      → 5
// executeIf(true,  x => "да-" + x, x => "нет-" + x, "ответ") → "да-ответ"
// executeIf(false, x => "да-" + x, x => "нет-" + x, "ответ") → "нет-ответ"
//
