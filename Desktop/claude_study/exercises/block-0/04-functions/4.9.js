// --- Описание ---
// Рекурсия — функция вызывает сама себя.
// Обязательно нужен базовый случай (условие выхода), иначе бесконечный цикл.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию factorial(n) — факториал числа.
//    factorial(5) = 5 * 4 * 3 * 2 * 1 = 120
//    Базовый случай: factorial(0) = 1, factorial(1) = 1
//
// 2. Напиши функцию fibonacci(n) — n-е число Фибоначчи.
//    fibonacci(0) = 0, fibonacci(1) = 1
//    fibonacci(n) = fibonacci(n-1) + fibonacci(n-2)
//
// 3. Напиши функцию sumRange(n) — сумма чисел от 1 до n.
//    sumRange(5) = 5 + 4 + 3 + 2 + 1 = 15
//
// 4. Напиши функцию flatten(arr) — разворачивает вложенный массив
//    любой глубины (без .flat()).
//    Если элемент — массив (Array.isArray), рекурсивно разверни его.
//
//    --- Объяснение для выполнения ---
//    Array.isArray(x) — проверяет, является ли x массивом (true/false)
//    Array.isArray([1,2])  → true
//    Array.isArray(5)      → false
//
//    Алгоритм:
//    1. Создай пустой массив result = []
//    2. Пройди циклом for...of по arr
//    3. Для каждого элемента:
//       - Если это массив (Array.isArray(element)) →
//         вызови flatten(element) рекурсивно и добавь результаты в result
//         через: result = result.concat(flatten(element))
//         (concat соединяет два массива в один новый, не меняя оригиналы)
//       - Если НЕ массив → добавь в result через result.push(element)
//    4. Верни result
//
//    Пошагово для flatten([1, [2, 3]]):
//    - элемент 1: не массив → result = [1]
//    - элемент [2, 3]: массив → вызываем flatten([2, 3])
//      - внутри: 2 не массив → [2], 3 не массив → [2, 3]
//      - возвращает [2, 3]
//    - result = [1].concat([2, 3]) = [1, 2, 3]
//
// --- Тесты ---
// factorial(0)  → 1
// factorial(5)  → 120
// factorial(10) → 3628800
//
// fibonacci(0)  → 0
// fibonacci(1)  → 1
// fibonacci(7)  → 13
// fibonacci(10) → 55
//
// sumRange(1)   → 1
// sumRange(5)   → 15
// sumRange(100) → 5050
//
// flatten([1, [2, 3], [4, [5, [6]]]]) → [1, 2, 3, 4, 5, 6]
// flatten([[1, 2], [[3]], 4])         → [1, 2, 3, 4]
//
// --- Ключевая концепция ---
// Рекурсия = базовый случай + рекурсивный случай.
// Без базового случая — бесконечный вызов → переполнение стека.

console.log("1. Напиши функцию factorial(n) — факториал числа.");
function factorial(n) {
  if (n <= 1) return 1;
  return n * factorial(n - 1);
}

console.log(factorial(0));
console.log(factorial(5));
console.log(factorial(10));

console.log("2. Напиши функцию fibonacci(n) — n-е число Фибоначчи.");
function fibonacci(n) {
  if (n === 0) return 0;
  if (n === 1) return 1;
  return fibonacci(n - 1) + fibonacci(n - 2);
}

console.log(fibonacci(0));
console.log(fibonacci(1));
console.log(fibonacci(7));
console.log(fibonacci(10));

console.log("3. Напиши функцию sumRange(n) — сумма чисел от 1 до n.");
function sumRange(n) {
  if (n === 0) return 0;
  if (n === 1) return 1;
  return n + sumRange(n - 1);
}

console.log(sumRange(1));
console.log(sumRange(5));
console.log(sumRange(100));

console.log("4. Напиши функцию flatten(arr) — разворачивает вложенный массив");

function flatten(arr) {
  let result = [];
  for (const element of arr) {
    if (Array.isArray(element)) {
         result = result.concat(flatten(element))
    }else{
        result.push(element)
    }
  }
  return result
}

console.log(flatten([1, [2, 3], [4, [5, [6]]]]));
console.log(flatten([[1, 2], [[3]], 4]));

