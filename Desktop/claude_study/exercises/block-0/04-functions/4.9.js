// --- Пошаговый разбор (что происходит технически) ---
//
// factorial(5):
// Шаг 1: 5 <= 1? нет → return 5 * factorial(4)
// Шаг 2: 4 <= 1? нет → return 4 * factorial(3)
// Шаг 3: 3 <= 1? нет → return 3 * factorial(2)
// Шаг 4: 2 <= 1? нет → return 2 * factorial(1)
// Шаг 5: 1 <= 1? ДА → return 1 (базовый случай!)
// Раскрутка: 2*1=2 → 3*2=6 → 4*6=24 → 5*24=120
//
// fibonacci(7):
// fib(7) = fib(6) + fib(5)
// fib(6) = fib(5) + fib(4) ... и так далее
// Базовые случаи: fib(0) = 0, fib(1) = 1
// Дерево вызовов разрастается (каждый вызов порождает 2 новых)
// Результат: 13 (0, 1, 1, 2, 3, 5, 8, 13)
//
// sumRange(5):
// Шаг 1: 5 === 0? нет, 5 === 1? нет → return 5 + sumRange(4)
// Шаг 2: return 4 + sumRange(3)
// Шаг 3: return 3 + sumRange(2)
// Шаг 4: return 2 + sumRange(1)
// Шаг 5: 1 === 1? ДА → return 1
// Раскрутка: 2+1=3 → 3+3=6 → 4+6=10 → 5+10=15
//
// flatten([1, [2, 3], [4, [5, [6]]]]):
// Шаг 1: result = []
// Шаг 2: элемент 1 — не массив → result.push(1) → result = [1]
// Шаг 3: элемент [2, 3] — массив → flatten([2, 3])
//   Внутри: 2 → push → [2], 3 → push → [2, 3], return [2, 3]
//   result = [1].concat([2, 3]) = [1, 2, 3]
// Шаг 4: элемент [4, [5, [6]]] — массив → flatten([4, [5, [6]]])
//   Внутри: 4 → push → [4]
//   [5, [6]] — массив → flatten([5, [6]])
//     Внутри: 5 → push → [5]
//     [6] — массив → flatten([6])
//       Внутри: 6 → push → [6], return [6]
//     [5].concat([6]) = [5, 6], return [5, 6]
//   [4].concat([5, 6]) = [4, 5, 6], return [4, 5, 6]
//   result = [1, 2, 3].concat([4, 5, 6]) = [1, 2, 3, 4, 5, 6]
// Шаг 5: return [1, 2, 3, 4, 5, 6]
//
// --- Ошибки при решении ---
// Ошибка 1: fibonacci — написал n = (n-1) + (n-2) вместо fibonacci(n-1) + fibonacci(n-2)
// Почему неправильно: n-1 + n-2 это просто арифметика (5-1 + 5-2 = 7), не рекурсия.
//   Рекурсия = функция вызывает САМА СЕБЯ. Нужен вызов fibonacci(...).
// Как мыслить: "Рекурсия = я вызываю себя с меньшим входом, пока не дойду до базового случая"
//
// Ошибка 2: sumRange — написал return sumRange(n-1) без + n
// Почему неправильно: sumRange(n-1) вернёт сумму от 1 до n-1. Но нужна сумма от 1 до n!
//   Забыл прибавить текущее n к результату.
// Как правильно: return n + sumRange(n-1) — "я (n) + сумма всех до меня"
// Как мыслить: рекурсивный случай = "моя часть работы" + "рекурсия для остального"
//
// Ошибка 3: flatten — return внутри цикла (выходил из функции на первом вложенном массиве)
// Почему неправильно: return прерывает ВСЮ функцию, не только текущую итерацию.
//   Если написать return result.concat(flatten(element)) внутри if — обработается
//   только первый массив, остальные элементы пропустятся.
// Как правильно: result = result.concat(...) — сохраняем в result, а return — ПОСЛЕ цикла.
// Как мыслить: return в цикле = "я закончил на первом элементе". Нужно ли тебе это?
//
// --- Почему решение правильное ---
// factorial: базовый случай n<=1→1, рекурсивный n*factorial(n-1) — корректная формула
// fibonacci: два базовых (0→0, 1→1), рекурсивный fib(n-1)+fib(n-2) — определение Фибоначчи
// sumRange: базовые (0→0, 1→1), рекурсивный n+sumRange(n-1) — "я + сумма остальных"
// flatten: для каждого элемента — если массив, рекурсивно развернуть; если нет, добавить.
//   concat не мутирует result — создаёт новый массив. return ПОСЛЕ цикла — все элементы обработаны.
//
// --- Что должен был усвоить ---
// 1. Рекурсия = базовый случай (когда остановиться) + рекурсивный случай (вызов себя)
// 2. В рекурсивном случае ОБЯЗАТЕЛЬНО есть вызов самой функции
// 3. "Моя часть" + рекурсия(остальное) — универсальный паттерн
// 4. return в цикле — выход из ВСЕЙ функции, не из итерации
