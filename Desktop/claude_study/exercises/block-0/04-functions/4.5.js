console.log("1. Напиши функцию sum(...nums) — сумма всех аргументов.");

function sum(...nums) {
  let total = 0;
  for (const n of nums) {
    total += n;
  }
  return total;
}
console.log(sum(1, 2, 3, 4, 5));
console.log(sum());

console.log("2. Напиши функцию max(...nums) — максимум из всех аргументов.");

function max(...nums) {
  let best = nums[0];
  for (const n of nums) {
    if (n > best) {
      best = n;
    }
  }
  return best;
}

console.log(max(3, 1, 4, 1, 5, 9));

console.log("3. Напиши функцию joinWords(separator, ...words)");

function joinWords(separator, ...words) {
  let result = "";
  for (const n of words) {
    if (result !== "") {
      result = result + separator;
    }
    result += n;
  }
  return result;
}

console.log(joinWords("-", "2026", "03", "13"));
console.log(joinWords(" ", "Привет", "мир"));

