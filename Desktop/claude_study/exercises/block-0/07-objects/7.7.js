// --- Описание ---
// pick — извлекает только указанные ключи.
// omit — убирает указанные ключи.
// Эти функции есть в lodash, но полезно уметь писать самому.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию pick(obj, keys):
//    - Возвращает новый объект с ТОЛЬКО указанными ключами
//    - Если ключа нет в obj — пропускает
//
// 2. Напиши функцию omit(obj, keys):
//    - Возвращает новый объект БЕЗ указанных ключей
//    - Два способа: через Object.entries + filter, или через деструктуризацию
//
// 3. Используй pick и omit для реальной задачи:
//    const user = { id: 1, name: "Игорь", password: "secret", email: "i@mail.ru", role: "admin" };
//    - Публичные данные: pick(user, ["id", "name", "email"])
//    - Безопасные данные: omit(user, ["password"])
//
// --- Тесты ---
// pick({ a: 1, b: 2, c: 3, d: 4 }, ["a", "c"])
//   → { a: 1, c: 3 }
//
// pick({ name: "Игорь" }, ["name", "age"])
//   → { name: "Игорь" }  (age нет — пропущен)
//
// omit({ a: 1, b: 2, c: 3 }, ["b"])
//   → { a: 1, c: 3 }
//
// omit({ name: "Игорь", password: "secret", age: 25 }, ["password"])
//   → { name: "Игорь", age: 25 }
//
// --- Ключевая концепция ---
