// --- Описание ---
// Обычный spread { ...obj1, ...obj2 } перезаписывает вложенные объекты целиком.
// deepMerge — рекурсивно сливает вложенные объекты.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию deepMerge(obj1, obj2):
//    - Для каждого ключа obj2:
//      - Если значение в обоих — объект → рекурсивно мержи
//      - Иначе → значение из obj2 перезаписывает obj1
//    - Верни НОВЫЙ объект (не мутируй входные)
//
// 2. Покажи разницу между spread merge и deepMerge:
//    const a = { x: 1, nested: { a: 1, b: 2 } };
//    const b = { x: 2, nested: { b: 99, c: 3 } };
//    // Spread: { ...a, ...b } → nested из b перезаписывает целиком
//    // deepMerge: nested сливается → { a: 1, b: 99, c: 3 }
//
// --- Тесты ---
// deepMerge(
//   { a: 1, b: { x: 1, y: 2 } },
//   { b: { y: 99, z: 3 }, c: 4 }
// )
// → { a: 1, b: { x: 1, y: 99, z: 3 }, c: 4 }
//
// deepMerge(
//   { theme: { colors: { primary: "blue" }, font: 14 } },
//   { theme: { colors: { secondary: "red" } } }
// )
// → { theme: { colors: { primary: "blue", secondary: "red" }, font: 14 } }
//
// 3. (НАКОПИТЕЛЬНОЕ: объекты + массивы + строки + функции)
//    Напиши функцию mergeConfigs(...configs):
//    - Принимает ЛЮБОЕ количество конфигов (rest-параметр)
//    - Если один из конфигов не объект — пропусти его (typeof !== "object" || config === null)
//    - Последовательно deepMerge-ит их слева направо (используй свой deepMerge)
//    - Поддерживает массивы: если значение — массив в обоих, КОНКАТЕНИРУЙ (не перезаписывай)
//    - Строковые ключи "env" преобразуй в upperCase в финальном результате
//    - Если в конфиге есть ключ "debug" — приведи значение к Boolean() (преобразование типов)
//    - Для ключа "port": если значение строка — приведи к Number() (преобразование типов)
//
// mergeConfigs(
//   { db: { host: "localhost", port: "5432" }, plugins: ["auth"], debug: 1 },
//   { db: { port: 3306 }, plugins: ["cache"], env: "dev", debug: "" },
//   { db: { name: "mydb" }, plugins: ["logger"] }
// )
// → {
//     db: { host: "localhost", port: 3306, name: "mydb" },
//     plugins: ["auth", "cache", "logger"],
//     env: "DEV",
//     debug: false       // Boolean("") → false
//   }
//
// mergeConfigs(null, { a: 1 }, "строка", { b: 2 })
// → { a: 1, b: 2 }  (null и "строка" пропущены — не объекты)
