// 📖 Раздел учебника: "Destructuring" (07-objects.md)
//
// --- Описание ---
// Деструктуризация — извлечение свойств объекта в отдельные переменные.
// Можно переименовывать, задавать дефолты, извлекать вложенные свойства.
//
// --- Что нужно сделать ---
//
// 1. Базовая деструктуризация:
//    const product = { title: "Ноутбук", price: 50000, category: "Электроника" };
//    Извлеки title, price, category в отдельные переменные.
//
// 2. Переименование:
//    Из того же product извлеки title как name.
//    { title: name } = product
//
// 3. Значения по умолчанию:
//    const product2 = { title: "Мышь", price: 1500 };
//    Деструктурируй с дефолтами: category = "Разное", inStock = true
//
// 4. Rest в деструктуризации:
//    const user = { name: "Игорь", age: 25, role: "admin", city: "Москва" };
//    Извлеки name, а остальное собери в rest через { name, ...rest }
//
// 5. Деструктуризация в параметрах функции:
//    Напиши функцию formatUser({ name, age, role = "user" }):
//    Возвращает "Имя (возраст) — роль"
//
// --- Тесты ---
// После шага 1: title = "Ноутбук", price = 50000
// После шага 2: name = "Ноутбук" (переименовали)
// После шага 3: category = "Разное", inStock = true (дефолты)
// После шага 4: name = "Игорь", rest = { age: 25, role: "admin", city: "Москва" }
//
// formatUser({ name: "Игорь", age: 25 })         → "Игорь (25) — user"
// formatUser({ name: "Анна", age: 30, role: "admin" }) → "Анна (30) — admin"
//
// --- Ключевая концепция ---
// { prop } = obj — извлечение
// { prop: newName } = obj — переименование
// { prop = default } = obj — дефолт если undefined
// { a, ...rest } = obj — остальные свойства
