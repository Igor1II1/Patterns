// --- Описание ---
// call(obj, arg1, arg2) — вызывает с указанным this, аргументы через запятую.
// apply(obj, [arg1, arg2]) — то же, но аргументы массивом.
// bind(obj) — возвращает НОВУЮ функцию с привязанным this.
//
// --- Что нужно сделать ---
//
// function introduce(greeting, punctuation) {
//   return `${greeting}, я ${this.name}${punctuation}`;
// }
//
// const igor = { name: "Игорь" };
// const anna = { name: "Анна" };
//
// 1. Вызови introduce для igor через call: "Привет" и "!"
// 2. Вызови introduce для anna через apply: "Хай" и "."
// 3. Создай функцию igorIntroduce через bind (this = igor навсегда)
// 4. Вызови igorIntroduce("Здравствуйте", ".")
//
// 5. Объясни в комментарии: чем call отличается от apply?
//    Когда какой использовать?
//
// --- Тесты ---
// introduce.call(igor, "Привет", "!")  → "Привет, я Игорь!"
// introduce.apply(anna, ["Хай", "."]) → "Хай, я Анна."
// igorIntroduce("Здравствуйте", ".") → "Здравствуйте, я Игорь."
//
// --- Ключевая концепция ---
// call/apply — ВЫЗЫВАЮТ сразу с нужным this.
// bind — ВОЗВРАЩАЕТ новую функцию (не вызывает).
