// Почему так:    const — "значение не изменится", let — "значение может измениться".
//                Правильный выбор — обязанность разработчика, JS не проверяет это за тебя.
// Для чего:      const защищает от случайного переназначения и сигнализирует читателю кода:
//                "это значение не меняется". Код становится предсказуемым.
// Главное понять: по умолчанию используй const. Переключайся на let только если
//                переменная будет изменяться. Это снижает количество багов.
