//   - имя пользователя (не меняется после входа)
//   - возраст (может обновляться)
//   - активен ли аккаунт — true/false
//   - роль пользователя — по умолчанию "user" (не меняется)
//   - счётчик попыток входа (увеличивается при каждой попытке)
// После объявления — выведи все пять переменных через console.log.
//
// Раздел учебника: "Объявление переменных" → const/let/var

// по заданию не меняется значение поэтому const
const firstName = "Igor";
// возраст может обновляться поэтому let
let age = 27;
// статус может меняться, поэтому let
let status = true;
// роль по заданию не меняется
const role = "user";
// количество попыток меняется
let attempts = 0;

console.log(firstName);
console.log(age);
console.log(status);
console.log(role);
console.log(attempts);

