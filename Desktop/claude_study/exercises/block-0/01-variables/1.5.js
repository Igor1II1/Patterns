alert("hi");
let name = prompt("what is your name?")
let yourAge = Number(prompt("what is your age?"))
let notice = confirm("Do you want get a notice?")

alert(`Имя: ${name}, Возраст: ${yourAge}, Уведомления: ${notice}`)

