// Раздел учебника: "Операторы сравнения: == и ==="

console.log(0 == false);      // true  приводит типы, оба становятся числом 0
console.log(0 === false);     // false проводит строгое сравнение типов
console.log("" == false);     // true  оба приводит в число
console.log(null == undefined); // true особое правило js
console.log(null === undefined); // false строгое сравнение типов
console.log(NaN == NaN);     // false потому что это исключение в js



function checkNaN(value){
    if(Number.isNaN(value)){
        return true
    }else{
        return false
    }
}


console.log(checkNaN(42))
console.log(checkNaN("42"))
console.log(checkNaN(""))
console.log(checkNaN(NaN))

