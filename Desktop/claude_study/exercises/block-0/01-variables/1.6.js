// Что делали:    сравнивали ?? и || на одинаковых значениях, написали функцию с цепочкой ??.
// Почему так:    || считает ложными 0, "", false — и заменяет их резервным значением.
//                ?? считает ложными только null и undefined — 0, false, "" оставляет как есть.
// Для чего:      правильный выбор оператора защищает от случайной замены валидных значений.
//                Пример: nickname: "" — это реальный пустой никнейм, а не "нет данных".
//                || бы заменил "" на email. ?? — нет.
// Главное понять: когда 0, false или "" — это ВАЛИДНЫЕ значения → используй ??.
//                Когда нужно заменить любое ложное значение → используй ||.
