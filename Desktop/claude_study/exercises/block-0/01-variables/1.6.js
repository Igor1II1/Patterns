// Шаг A — предсказание результатов || vs ??:
//   0 || "default"      → "default"  (0 — falsy, || берёт правую)
//   0 ?? "default"      → 0          (?? берёт правую только при null/undefined)
//   "" || "нет имени"   → "нет имени" ("" — falsy)
//   "" ?? "нет имени"   → ""          ("" не null и не undefined)
//   null || "нет данных"→ "нет данных"
//   null ?? "нет данных"→ "нет данных"
//   false || "запасной" → "запасной"
//   false ?? "запасной" → false
//
// Шаг B — напиши функцию getDisplayName(user):
//   возвращает user.nickname если есть, иначе user.email, иначе "Аноним"
//   Используй ?? (не ||) — потому что пустой nickname "" это валидное значение,
//   и || бы его заменил, а ?? — нет.
//
// Раздел учебника: "Оператор нулевого слияния ??", "Логические операторы"

function getDisplayName(user) {
    return user.nickname ?? user.email ?? "Аноним"
}

console.log(getDisplayName({ nickname: "igor123", email: "igor@mail.ru" })) // "igor123"
console.log(getDisplayName({ nickname: "", email: "igor@mail.ru" }))        // ""
console.log(getDisplayName({ email: "igor@mail.ru" }))                      // "igor@mail.ru"
console.log(getDisplayName({}))                                             // "Аноним"

