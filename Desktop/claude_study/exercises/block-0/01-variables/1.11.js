// Напиши функцию shallowEqual(a, b) — сравнение по содержимому, не по ссылке.
// Проблема: {a:1} === {a:1} = false (два разных объекта в памяти).
// Алгоритм:
//   1. Оба примитива (typeof !== "object") -> сравни через ===
//   2. Один примитив, другой объект -> return false
//   3. Оба объекты/массивы -> сравни через JSON.stringify(a) === JSON.stringify(b)
// Раздел учебника: "Копирование по значению vs по ссылке"

function shallowEqual(a,b){
 if (typeof a !=="object" && typeof b !=="object"){
    return a===b
 }else if(typeof a =="object" && typeof b !=="object"){
    return false
 }else if(typeof a ==="object" && typeof b ==="object"){
    return JSON.stringify(a) === JSON.stringify(b)
 }else{
    return false
 }
}



console.log(shallowEqual(1, 1));             // true
console.log(shallowEqual("a", "a"));         // true
console.log(shallowEqual(1, "1"));           // false — разные типы
console.log(shallowEqual([1, 2], [1, 2]));   // true
console.log(shallowEqual({a: 1}, {a: 1}));   // true
console.log(shallowEqual([1, 2], [1, 3]));   // false
console.log(shallowEqual({a: 1}, {a: 2}));   // false
console.log(shallowEqual(1, {a: 1}));        // false — примитив vs объект

