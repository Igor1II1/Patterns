// Что делали:    функция shallowEqual — сравнение двух значений по содержимому, а не по ссылке.
// Почему так:    === для объектов сравнивает адреса в памяти, не содержимое.
//                {a:1} === {a:1} = false — два разных объекта с одинаковым содержимым.
//                JSON.stringify() превращает объект в строку-снимок, строки сравниваются по значению.
// Для чего:      часто нужно сравнить содержимое двух объектов (формы, настройки, данные с сервера).
//                Стандартного способа в JS нет — приходится писать свой.
// Главное понять: примитивы сравниваются по значению (===), объекты — по ссылке (адресу).
//                JSON.stringify — простой способ сравнить содержимое, но не идеальный
//                (не работает с undefined, функциями, порядок ключей может отличаться).
//
// Логика 4 веток:
//   if:       a НЕ объект  &&  b НЕ объект   → оба примитива      → сравни ===
//   else if:  a объект     &&  b НЕ объект   → a объект, b прим.   → false
//   else if:  a объект     &&  b объект      → оба объекта         → JSON.stringify
//   else:     a НЕ объект, b объект (единственная оставшаяся комбинация) → false
//
//   2-й else if и else делают одно и то же — "один примитив, другой объект = false".
//   Просто 2-й ловит "a объект, b примитив", а else — обратное "a примитив, b объект".
//   Можно объединить через ||:
//     if (typeof a !== "object" || typeof b !== "object") return false;
//
// Как JSON.stringify сравнивает содержимое:
//   Шаг 1: JSON.stringify превращает объект в строку-снимок
//          [1, 2]   → '[1,2]'      {a: 1}  → '{"a":1}'
//          [1, 3]   → '[1,3]'      {a: 2}  → '{"a":2}'
//
//   Шаг 2: Теперь сравниваем не объекты, а строки
//          '[1,2]' === '[1,3]'     '{"a":1}' === '{"a":2}'
//
//   Шаг 3: Строки — примитивы, === сравнивает их посимвольно
//          '[1,2]' vs '[1,3]'  →  '[' == '[', '1' == '1', ',' == ',', '2' != '3'  →  false
//          '{"a":1}' vs '{"a":2}'  →  ... '1' != '2'  →  false
//
//   JSON.stringify — это "мост": объекты (сравнение по ссылке) → строки (сравнение по значению).
//   Без него === для объектов сравнивает только адреса в памяти, а не содержимое.