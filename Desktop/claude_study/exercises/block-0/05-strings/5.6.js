//    Напиши функцию formatTable(headers, rows, format):
//    - headers — массив строк (названия столбцов)
//    - rows — массив массивов строк (данные)
//    - format — необязательный параметр: "compact" или "wide" (если не передан — используй ?? "wide")
//    - Каждый столбец выровнен по самому длинному значению (padEnd)
//    - Между столбцами разделитель: format === "compact" ? " | " : "  |  " (тернарный оператор)
