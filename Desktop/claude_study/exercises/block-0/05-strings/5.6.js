//
// --- Тесты ---
// formatTime(9, 5, 3)    → "09:05:03"
// formatTime(14, 30, 0)  → "14:30:00"
//
// maskCard("4111111111111111") → "**** **** **** 1111"
// maskCard("5500000000000004") → "**** **** **** 0004"
//
