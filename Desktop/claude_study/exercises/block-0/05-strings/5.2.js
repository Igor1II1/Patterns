//
// --- Что нужно сделать ---
//
// 1. Напиши функцию normalizeInput(str):
//    - Убирает пробелы по краям (trim)
//    - Переводит в нижний регистр
//    - Делает первую букву заглавной
//    - Возвращает результат
//
// 2. Напиши функцию compareIgnoreCase(str1, str2):
//    - Возвращает true если строки равны без учёта регистра
//    - trim обе строки перед сравнением
//
// 3. Напиши функцию cleanSpaces(str):
//    - trimStart() — убирает пробелы только слева
//    - trimEnd() — убирает пробелы только справа
//    - Покажи разницу между trim, trimStart, trimEnd
//
// --- Тесты ---
// normalizeInput("  ИГОРь  ")          → "Игорь"
// normalizeInput("  javaScript  ")     → "Javascript"
//
// compareIgnoreCase("Hello", "hello")           → true
// compareIgnoreCase("  ТЕСТ  ", " тест")        → true
// compareIgnoreCase("abc", "def")               → false
//
// cleanSpaces("  hello  ").trimStart()  → "hello  "
// cleanSpaces("  hello  ").trimEnd()    → "  hello"
// cleanSpaces("  hello  ").trim()       → "hello"
//
// --- Ключевая концепция ---
// Методы строк не мутируют — всегда возвращают новую строку.
