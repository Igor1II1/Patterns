// --- Что нужно сделать ---
//
// 1. Напиши функцию createUser(name, age):
//    Проверь и брось ошибку если:
//    - name не строка → throw new TypeError("Имя должно быть строкой")
//    - name пустая строка → throw new TypeError("Имя не может быть пустым")
//    - age не число → throw new TypeError("Возраст должен быть числом")
//    - age < 0 или age > 150 → throw new RangeError("Возраст от 0 до 150")
//    - age дробное → throw new RangeError("Возраст должен быть целым")
//    Если всё ок → return { name: name.trim(), age }
//
// 2. Вызови createUser с разными аргументами, оберни в try/catch:
