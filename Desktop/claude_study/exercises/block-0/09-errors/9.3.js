//    - createUser("", 25)        — TypeError
//    - createUser("Анна", -1)    — RangeError
//    - createUser("Борис", "25") — TypeError
//    В catch выведи: error.constructor.name + ": " + error.message
//
// --- Тесты ---
// createUser("Игорь", 25)   → { name: "Игорь", age: 25 }
// createUser("", 25)         → TypeError: Имя не может быть пустым
// createUser("Анна", -1)     → RangeError: Возраст от 0 до 150
// createUser("Борис", "25")  → TypeError: Возраст должен быть числом
// createUser("Мария", 25.5)  → RangeError: Возраст должен быть целым
//
// --- Ключевая концепция ---
// throw — программист СОЗНАТЕЛЬНО бросает ошибку для валидации.
