// --- Описание ---
// Retry — повторяет вызов функции при ошибке заданное число раз.
// Полезно для сетевых запросов, временных сбоев.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию retry(fn, times):
//    - Вызывает fn()
//    - Если fn бросает ошибку — пробует ещё раз (до times попыток)
//    - Если все попытки исчерпаны — бросает последнюю ошибку
//    - Если fn вернула результат — возвращает его
//    - Подсказка: используй цикл for + try/catch
//
// 2. Проверь на "нестабильной" функции:
//
// let attempts = 0;
// function unstable() {
//   attempts++;
//   if (attempts < 3) throw new Error("Временная ошибка");
//   return "Успех!";
// }
//
// 3. (Накопительное: функции + массивы + объекты + строки)
//    Напиши функцию retryWithLog(fn, times, options):
//    - Работает как retry, но ведёт журнал попыток
//    - options — необязательный параметр (если не передан — используй ?? {})
//      options.verbose — подробный лог (по умолчанию ?? true)
//      options.label — метка для логов (по умолчанию ?? "Операция")
//    - Перед началом проверь: typeof fn !== "function" — верни { success: false, result: null, attempts: 0, log: ["Ошибка: fn не функция"] }
//    - times должен быть числом > 0, если нет — используй || 1 (значение по умолчанию через short-circuit)
//    - Возвращает объект:
//      {
//        success: true/false,
//        result: значение (если успех) или null,
//        attempts: число попыток,
//        log: ["[Операция] Попытка 1: ошибка — Временная ошибка", "[Операция] Попытка 2: успех"]
//      }
//    - log — массив строк, описывающих каждую попытку
//    - Если verbose === true: "[label] Попытка N: ошибка — " + error.message
//    - Если verbose === false: короткий формат — "N: fail" / "N: ok"
//    - Если все попытки неудачны — НЕ бросай ошибку, верни объект с success: false
//
//    Подсказка: используй массив для log, цикл for, try/catch,
//    typeof, ??, ||, тернарный оператор для формата лога
//
// 4. Протестируй retryWithLog:
//    attempts = 0;
//    const result1 = retryWithLog(unstable, 5, { label: "Загрузка" });
//    console.log(result1);
//    // → { success: true, result: "Успех!", attempts: 3,
//    //     log: ["[Загрузка] Попытка 1: ошибка — Временная ошибка",
//    //           "[Загрузка] Попытка 2: ошибка — Временная ошибка",
//    //           "[Загрузка] Попытка 3: успех"] }
//
//    attempts = 0;
//    const result2 = retryWithLog(unstable, 1);
//    console.log(result2);
//    // → { success: false, result: null, attempts: 1,
//    //     log: ["[Операция] Попытка 1: ошибка — Временная ошибка"] }  // label по умолчанию
//
//    const result3 = retryWithLog(unstable, 3, { verbose: false });
//    // → log: ["1: fail", "2: fail", "3: ok"]  // короткий формат
//
//    const result4 = retryWithLog("не функция", 3);
//    // → { success: false, result: null, attempts: 0, log: ["Ошибка: fn не функция"] }
//
// --- Тесты задания 2 ---
// attempts = 0;
// retry(unstable, 5)  → "Успех!" (с 3-й попытки)
//
// attempts = 0;
// retry(unstable, 1)  → бросит Error("Временная ошибка") (попыток не хватило)
//
// retry(() => 42, 3)  → 42 (с первой попытки)
//
// --- Тесты задания 3-4 ---
