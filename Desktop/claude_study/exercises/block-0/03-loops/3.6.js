// ========== Задание 3.6 ==========
// Напиши функцию fizzbuzz(n) которая возвращает массив от 1 до n, где:
// - числа кратные 3 заменены на "Fizz"
// - числа кратные 5 заменены на "Buzz"
// - числа кратные 3 И 5 — на "FizzBuzz"
//
// fizzbuzz(15);
// [1, 2, "Fizz", 4, "Buzz", "Fizz", 7, 8, "Fizz", "Buzz",
//  11, "Fizz", 13, 14, "FizzBuzz"]
//
// Раздел учебника: "Цикл for" + "break и continue"
//
// Забегая вперёд — функции (глава 4):
// function имяФункции(параметр) {
//   const result = [];        // создаём пустой массив
//   // ... заполняем result в цикле ...
//   return result;            // возвращаем результат
// }
//
// Забегая вперёд — массивы (глава 6):
// result.push(значение) — добавить элемент в конец массива

function fizzbuzz(n) {
  const result = [];
  for (let i = 1; i <= n; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
      result.push("FizzBuzz");
    } else if (i % 5 === 0) {
      result.push("Buzz");
    } else if (i % 3 === 0) {
      result.push("Fizz");
    } else {
      result.push(i);
    }
  }
  return result
}

console.log(fizzbuzz(100));

