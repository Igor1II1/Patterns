// ========== Задание 3.2 ==========
// Используя for...of пройдись по массиву и:
// 1. Выведи название каждого товара
// 2. Найди общую стоимость
// 3. Выведи только товары дороже 5000
//
// Раздел учебника: "for...of — перебор элементов коллекции"
//
// Забегая вперёд — объекты (глава 7):
// { name: "Ноутбук", price: 50000 } — объект с полями name и price
// product.name — доступ к полю name
// product.price — доступ к полю price

const products = [
  { name: "Ноутбук", price: 50000 },
  { name: "Мышь", price: 1500 },
  { name: "Клавиатура", price: 3000 },
  { name: "Монитор", price: 25000 },
];



// 1. Названия
for (const { name, price } of products) {
  console.log(`${name}: ${price}`);
}

// 2. Общая стоимость
let total = 0;
for (const { name, price } of products) {
  total += price;
}
console.log(`Итого: ${total}`);

// 3. Дороже 5000
for (const { name, price } of products) {
  if (price > 5000) {
    console.log(`${name}: ${price}`);
  }
}

