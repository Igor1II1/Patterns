// Почему так:    for...of даёт значения (целые объекты), а деструктуризация { name, price }
//                сразу достаёт нужные поля. Не нужно писать product.name — короче и чище.
// Для чего:      массив объектов — самая частая структура данных (список товаров, пользователей,
//                заказов). Уметь проходить и фильтровать — базовый навык.
// Главное понять: for...of + const работает потому что на каждой итерации создаётся НОВАЯ
//                переменная. total += price — паттерн "аккумулятор" для подсчёта суммы.
