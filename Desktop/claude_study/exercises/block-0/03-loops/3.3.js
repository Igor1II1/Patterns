// ========== Задание 3.3 ==========
// Используя while:
// 1. Выведи числа от 1 до 10
// 2. Найди первое число больше 100 которое делится на 7 и на 11
// 3. Посчитай сколько раз нужно умножить 2 на 2 чтобы получить число больше 1000
//
// Раздел учебника: "Цикл while"

let i = 1;
while (i <= 10) {
  console.log(i);
  i++;
}

let count = 101;
while (count > 100) {
  count++;
  if (count % 7 == 0 && count % 11 == 0) {
    console.log(count);
    break;
  }
}

let n = 2;
let result = 0;
while (n <= 1000) {
  n=n * 2;
  result++
}
console.log(result);

