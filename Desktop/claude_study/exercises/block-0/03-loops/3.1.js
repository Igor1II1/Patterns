// ========== Задание 3.1 ==========
// Используя цикл for:
// 1. Выведи числа от 1 до 20
// 2. Выведи только чётные числа от 1 до 20
// 3. Выведи числа от 10 до 1 в обратном порядке
// 4. Найди сумму чисел от 1 до 100
//
// Раздел учебника: "Цикл for"


for(let i=1; i<=20; i++){
    console.log(i);
}


for (let i = 1; i<=20; i++){
    if(i%2==0){
        console.log(i)
    }
}

for (let i = 10; i>=1; i--){
    console.log(i)
}


let sum =0;
for(let i = 1; i<=100; i++){
    sum +=i
    
}

console.log(sum)

// ========== Разбор задания ==========
// Что делали:    4 упражнения на базовый for — прямой перебор, фильтрация, обратный порядок, накопление суммы.
// Почему так:    for(init; condition; step) — универсальная конструкция. Меняя condition и step,
//                управляем направлением и диапазоном перебора.
// Для чего:      for — основа всех циклов. Перебор элементов, подсчёт, фильтрация — встречается везде.
// Главное понять: i <= 20 перебирает включительно (1..20). i-- уменьшает счётчик для обратного порядка.
//                Для обратного цикла: начинай с большого, условие >= малого, шаг i--.
//                ⚠️ Ошибка на сессии: путал >= и <= в обратном цикле. Совет: проговори вслух
//                "i начинается с 10, пока i больше или равно 1, уменьшаем".