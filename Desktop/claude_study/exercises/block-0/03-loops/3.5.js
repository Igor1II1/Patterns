// ========== Задание 3.5 ==========
// Используй break и continue:
// 1. Найди первое число больше 6 и остановись (break)
// 2. Выведи все числа кроме чётных (continue)
// 3. Найди сумму чисел до первого числа больше 7
//
// Раздел учебника: "break и continue"

const numbers = [3, 7, 2, 9, 1, 5, 8, 4, 6];

for (const n of numbers) {
  if (n > 6) {
    console.log(n);
    break;
  }
}

for (const n of numbers) {
  if (n % 2 === 0) {
    continue;
  }
  console.log(n);
}

console.log("===3===");

let result = 0;

for (const n of numbers) {
  if (n >7) break;
  result += n;
}

console.log(result);

// ========== Разбор задания ==========
// Что делали:    3 задачи на break и continue — досрочный выход, пропуск элементов, сумма с остановкой.
// Почему так:    break — останавливает цикл полностью (нашёл что искал — выходи).
//                continue — пропускает ТЕКУЩУЮ итерацию (не подходит — следующий).
// Для чего:      break — поиск первого подходящего элемента, остановка при ошибке.
//                continue — фильтрация "на лету" без вложенных if.
// Главное понять: continue пропускает то, что ВНУТРИ условия. if (n % 2 === 0) continue
//                пропускает чётные → остаются нечётные. Читай: "если чётное — пропусти".
//                ⚠️ Ошибка на сессии: написал n % 2 !== 0 — пропускал нечётные вместо чётных.
//                Совет: читай условие continue вслух: "если [условие] — пропусти этот элемент".
