// --- Описание ---
// map — трансформирует каждый элемент (1:1).
// filter — оставляет элементы по условию.
// reduce — сворачивает массив в одно значение.
//
// --- Что нужно сделать ---
//
// const numbers = [5, 3, 8, 1, 9, 2, 7, 4, 6];
//
// Используя методы массива (БЕЗ циклов):
// 1. map: удвой каждое число → [10, 6, 16, ...]
// 2. filter: получи только чётные → [8, 2, 4, 6]
// 3. reduce: найди сумму → 45
// 4. reduce: найди максимум → 9
// 5. map: преобразуй числа в строки "число: N" → ["число: 5", ...]
// 6. filter + reduce: найди сумму только нечётных чисел
//
// 7. Напиши функцию average(arr) через reduce:
//    - Возвращает средний балл
//
// --- Тесты ---
// [5,3,8,1,9,2,7,4,6].filter(n => n % 2 === 0) → [8, 2, 4, 6]
// [5,3,8,1,9,2,7,4,6].reduce((sum, n) => sum + n, 0) → 45
//
// average([80, 90, 70])  → 80
// average([100])         → 100
// average([])            → 0
//
// --- Ключевая концепция ---
// map: [a, b, c] → [f(a), f(b), f(c)] (длина не меняется)
// filter: [a, b, c] → элементы где условие true (длина может уменьшиться)
// reduce: [a, b, c] → одно значение (сумма, макс, объект, строка)
