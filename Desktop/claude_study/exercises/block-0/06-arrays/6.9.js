// --- Описание ---
// zip — объединяет элементы нескольких массивов попарно.
// chunk — разбивает массив на части фиксированного размера.
//
// --- Что нужно сделать ---
//
// 1. Напиши функцию zip(...arrays):
//    - Принимает любое количество массивов (rest)
//    - Возвращает массив кортежей (массивов)
//    - Длина результата = длина самого короткого входного массива
//
// 2. Напиши функцию chunk(arr, size):
//    - Разбивает массив на подмассивы размера size
//    - Последний подмассив может быть меньше size
//    - Используй slice внутри цикла
//
// 3. Напиши функцию flatten(arr):
//    - Разворачивает вложенный массив ЛЮБОЙ глубины
//    - БЕЗ использования .flat()
//    - Используй рекурсию + reduce или concat
//
// --- Тесты ---
// zip([1, 2, 3], ["a", "b", "c"])
//   → [[1, "a"], [2, "b"], [3, "c"]]
// zip([1, 2], ["a", "b", "c"], [true, false])
//   → [[1, "a", true], [2, "b", false]]
//
// chunk([1, 2, 3, 4, 5, 6, 7], 3) → [[1,2,3], [4,5,6], [7]]
// chunk([1, 2, 3, 4], 2)          → [[1,2], [3,4]]
// chunk([1, 2, 3], 5)             → [[1,2,3]]
//
// flatten([1, [2, 3], [4, [5, [6]]]]) → [1, 2, 3, 4, 5, 6]
//
// 4. (НАКОПИТЕЛЬНОЕ: массивы + строки + функции + циклы)
//    Напиши функцию wordFrequency(text, minCount):
//    - Принимает текст (строку) и минимальное количество вхождений (необязательно)
//    - Если text не передан или не строка — верни [] (используй typeof для проверки)
//    - minCount — порог: показывать только слова с частотой >= minCount (если не передан — используй ?? 1)
//    - Разбивает на слова (split по пробелу), приводит к нижнему регистру
//    - Пустые слова (falsy после split) — пропускай: if (!word) continue
//    - Возвращает массив пар [слово, количество], отсортированный по убыванию частоты
//    - Используй: split, toLowerCase, reduce (или цикл), Object.entries, sort, filter
//
// wordFrequency("кот и пёс и кот и кот")
//   → [["кот", 3], ["и", 3], ["пёс", 1]]
//
// wordFrequency("кот и пёс и кот и кот", 2)
//   → [["кот", 3], ["и", 3]]  (minCount = 2, "пёс" отфильтрован)
//
// wordFrequency("hello world hello")
//   → [["hello", 2], ["world", 1]]
//
// wordFrequency(null)     → []  (не строка)
// wordFrequency(undefined) → []  (не передан)
