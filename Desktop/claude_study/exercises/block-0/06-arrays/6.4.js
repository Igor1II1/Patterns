// Объекты подробно изучаются в теме 07-objects. Здесь достаточно знать:
//   - Объект = набор пар "ключ: значение": { id: 1, name: "Игорь" }
//   - Доступ через точку: user.name, user.age
//   - Массив может содержать объекты: [{ id: 1 }, { id: 2 }]
//   - В коллбэках (u => u.age) мы читаем свойство объекта через точку
//
// --- Описание ---
// find — находит ПЕРВЫЙ элемент по условию (или undefined).
// findIndex — находит ИНДЕКС первого (или -1).
// some — есть ли ХОТЯ БЫ ОДИН подходящий (boolean).
// every — ВСЕ ли подходят (boolean).
// forEach — перебирает, ничего не возвращает (побочные эффекты).
//
// --- Что нужно сделать ---
//
// const users = [
//   { id: 1, name: "Игорь", age: 25, active: true },
//   { id: 2, name: "Анна", age: 17, active: false },
//   { id: 3, name: "Борис", age: 30, active: true },
//   { id: 4, name: "Мария", age: 22, active: true },
// ];
//
// 1. find: найди пользователя с id === 3
// 2. find: найди первого неактивного пользователя
// 3. findIndex: найди индекс пользователя "Мария"
// 4. some: есть ли несовершеннолетние (age < 18)?
// 5. every: все ли пользователи активны?
// 6. forEach: выведи "Имя: ... (возраст: ...)" для каждого
//
// 7. Объясни в комментарии: чем forEach отличается от map?
//    Почему нельзя: const result = users.forEach(...)?
//
// --- Тесты ---
// users.find(u => u.id === 3) → { id: 3, name: "Борис", ... }
// users.find(u => u.id === 99) → undefined
// users.findIndex(u => u.name === "Мария") → 3
// users.some(u => u.age < 18) → true
// users.every(u => u.active) → false
//
// --- Ключевая концепция ---
// find/findIndex — поиск ОДНОГО элемента.
// some/every — проверка условия по ВСЕМ элементам (boolean).
// forEach — перебор с побочными эффектами (НЕ возвращает массив).
