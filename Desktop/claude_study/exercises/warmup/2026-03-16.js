// --- Карточка 3: ООП (просрочено) ---
// Задание: класс Animal с конструктором и методом speak()

class Animal{
    constructor(name, sound){
        this.name=name;
        this.sound=sound
    }
    speak(){
        return `${this.name} говорит ${this.sound}`
    }
}

let cat = new Animal("кошка", "мяу")
console.log(cat.speak()) // "кошка говорит мяу"

