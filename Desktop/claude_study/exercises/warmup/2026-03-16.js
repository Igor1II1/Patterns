// --- Карточка 1: Циклы (+7д) ---
// Задание: sumOdd(n) — сумма нечётных чисел от 1 до n

function sumOdd(n){
    let result=0;
    for(let i = 0; i<=n; i++){
        if(i%2!==0){
            result+=i
        }
    }
    return result
}

console.log(sumOdd(7)) // 16

