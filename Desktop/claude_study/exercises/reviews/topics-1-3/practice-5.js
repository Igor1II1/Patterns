//                Если данные невалидны — добавляет ошибку в массив.
//
// === Пошаговый разбор кода ===
//
// 1. for (let i = 0; i < users.length; i++)
//    Обычный for (не for...of), потому что нам нужен НОМЕР пользователя (i+1).
//    for...of не даёт индекс, а нам нужно писать "Пользователь 2: ..."
//
// 2. Три ОТДЕЛЬНЫХ if — каждый проверяет своё правило:
//
//    if (typeof users[i].name !== "string" || users[i].name === "")
//    → "Если имя НЕ строка ИЛИ пустая строка — ошибка"
//
//    if (users[i].age > 120 || users[i].age < 14)
//    → "Если возраст > 120 ИЛИ < 14 — ошибка"
//    → && тут нельзя: число не может быть одновременно > 120 И < 14
//
//    if (!users[i].email.includes("@") || !users[i].email.includes("."))
//    → "Если email НЕ содержит @ ИЛИ НЕ содержит точку — ошибка"
//    → ! переворачивает: includes("@") = true → !true = false (ок, идём дальше)
//
// === Закон де Моргана (почему || а не &&) ===
//
//    Мы хотим ошибку когда данные ПЛОХИЕ. Но проще думать "когда хорошие":
//
//    Валидный email:     includes("@") && includes(".")    ← "есть @ И есть ."
//    Невалидный email:  !includes("@") || !includes(".")   ← "нет @ ИЛИ нет ."
//
//    Правило переворачивания:
//    !(A && B) = !A || !B   — при добавлении ! оператор && меняется на ||
//    !(A || B) = !A && !B   — при добавлении ! оператор || меняется на &&
//
// === Полная трассировка ===
//
// Итерация 0 (i=0): users[0] = { name: "Игорь", age: 25, email: "igor@mail.ru" }
//   Имя:    typeof "Игорь" !== "string" → false, "Игорь" === "" → false
//           false || false → false → НЕТ ошибки ✓
//   Возраст: 25 > 120 → false, 25 < 14 → false
//           false || false → false → НЕТ ошибки ✓
//   Email:  !"igor@mail.ru".includes("@") → !true → false
//           !"igor@mail.ru".includes(".") → !true → false
//           false || false → false → НЕТ ошибки ✓
//   (Игорь валиден — ничего не добавлено)
//
// Итерация 1 (i=1): users[1] = { name: "", age: 25, email: "anna@mail.ru" }
//   Имя:    typeof "" !== "string" → false, "" === "" → true
//           false || true → true → ОШИБКА: "Пользователь 2: имя не может быть пустым"
//   Возраст и email — валидны, ошибок нет.
//
// Итерация 2 (i=2): users[2] = { name: "Петя", age: 10, email: "petya@mail.ru" }
//   Имя и email — валидны.
//   Возраст: 10 > 120 → false, 10 < 14 → true
//           false || true → true → ОШИБКА: "Пользователь 3: возраст..."
//
// Итерация 3 (i=3): users[3] = { name: "Маша", age: 30, email: "mashamail" }
//   Имя и возраст — валидны.
//   Email:  !"mashamail".includes("@") → !false → true
//           true || ... → true (даже не проверяем вторую часть)
//           → ОШИБКА: "Пользователь 4: неправильный email"
//
// Для чего:      Валидация форм — одна из самых частых задач в веб-разработке.
//                Каждое поле проверяется отдельно, чтобы пользователь видел ВСЕ ошибки сразу.
// Главное понять: Закон де Моргана — при переворачивании условий && ↔ || меняются местами.
