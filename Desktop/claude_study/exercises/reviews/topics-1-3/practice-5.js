// Напиши функцию validateUsers(users) которая проверяет массив пользователей
// и возвращает массив ошибок. Если ошибок нет — возвращает пустой массив.
//
// Правила проверки для каждого пользователя:
//   - name должен быть непустой строкой (typeof === "string" && name !== "")
//   - age должен быть числом от 14 до 120
//   - email должен содержать "@" и "." (str.includes)
//
// Если пользователь валиден — пропусти его (continue).
// Если невалиден — добавь строку с ошибкой в результат.
//
// Используй:
//   - for...of для перебора (тема 3)
//   - continue для пропуска валидных (тема 3)
//   - typeof, falsy-проверки (тема 1)
//   - if/else if, составные условия && || (тема 2)
//   - push для сборки ошибок
//
// const users = [
//   { name: "Игорь", age: 25, email: "igor@mail.ru" },
//   { name: "", age: 25, email: "anna@mail.ru" },
//   { name: "Петя", age: 10, email: "petya@mail.ru" },
//   { name: "Маша", age: 30, email: "mashamail" },
// ];
//
// validateUsers(users)
// → [
//   "Пользователь 2: имя не может быть пустым",
//   "Пользователь 3: возраст должен быть от 14 до 120",
//   "Пользователь 4: некорректный email",
// ]
//
// Подсказка: используй обычный for (не for...of) чтобы иметь номер пользователя (i+1).
//
// Забегая вперёд:
// str.includes("@")  — проверяет содержит ли строка символ (глава 5)
//
// Раздел учебника: "Цикл for" (js/03-loops.md) + "typeof" (js/01-variables.md)
//                  + "if/else" (js/02-conditions.md)

const users = [
  { name: "Игорь", age: 25, email: "igor@mail.ru" },
  { name: "", age: 25, email: "anna@mail.ru" },
  { name: "Петя", age: 10, email: "petya@mail.ru" },
  { name: "Маша", age: 30, email: "mashamail" },
];

function validateUsers(users) {
  const result = [];
  for (let i = 0; i < users.length; i++) {
    if (typeof users[i].name !== "string" || users[i].name === "") {
      result.push(`Пользователь ${i + 1}: имя не может быть пустым`);
    }
    if (users[i].age > 120 || users[i].age < 14) {
      result.push(`Пользователь ${i + 1}: возраст должен быть от 14 до 120`);
    }
    if (!users[i].email.includes("@") || !users[i].email.includes(".")) {
      result.push(`Пользователь ${i + 1}: неправильный email`);
    }
  }
  return result;
}

console.log(validateUsers(users));

