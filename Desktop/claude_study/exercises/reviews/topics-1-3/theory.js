// Q7: if + if + if vs if + else if + else if
// =====================================================================
//
// Ответ Игоря: ✅ ПРАВИЛЬНО
//
// if + if + if — три НЕЗАВИСИМЫХ проверки:
//   Каждый if проверяется ВСЕГДА, независимо от результата предыдущих.
//   Может сработать 0, 1, 2 или все 3.
//   Используй когда условия НЕ исключают друг друга.
//
// if + else if + else if — цепочка с ОДНИМ результатом:
//   Как только один сработал — остальные ПРОПУСКАЮТСЯ.
//   Сработает максимум 1 из всей цепочки.
//   Используй когда варианты ВЗАИМОИСКЛЮЧАЮЩИЕ (оценка A или B или C).
//
// Пример:
//   let x = 15;
//
//   // Независимые if — сработают ОБА:
//   if (x > 10) console.log("больше 10");   // ✓ сработает
//   if (x > 5)  console.log("больше 5");    // ✓ тоже сработает
//
//   // Цепочка — сработает ТОЛЬКО ПЕРВЫЙ:
//   if (x > 10) console.log("больше 10");       // ✓ сработает
//   else if (x > 5) console.log("больше 5");    // пропущен!
//
// Замечание к ответу:
//   Игорь написал "параллельно" — правильнее "независимо".
//   "Параллельно" в программировании означает "одновременно" (многопоточность).
//   Здесь if-ы выполняются ПОСЛЕДОВАТЕЛЬНО, но НЕЗАВИСИМО друг от друга.
