// Q6: ||, ??, && — что вернёт каждое
// =====================================================================
//
// Ответ Игоря: ⚠️ 3 из 4 правильно
//
// "" || "дефолт"       → "дефолт"  ✅
//   || возвращает ПЕРВЫЙ truthy (или последний если все falsy)
//   "" — falsy → идём дальше → "дефолт" — truthy → возвращаем "дефолт"
//
// 0 ?? "дефолт"        → 0  ✅
//   ?? проверяет только null/undefined (не все falsy!)
//   0 — не null и не undefined → возвращаем 0
//   (если бы было ||: 0 || "дефолт" → "дефолт", потому что 0 falsy)
//
// null && "привет"     → null  ← Игорь написал "false" ❌
//   && возвращает ПЕРВЫЙ falsy (или последний если все truthy)
//   null — falsy → сразу возвращаем null (не false, а именно null!)
//   && НЕ превращает значения в boolean. Он возвращает САМО значение.
//   null && что_угодно → null
//   false && что_угодно → false
//   0 && что_угодно → 0
//   Каждый раз возвращается само falsy-значение, не false!
//
// "a" && "b" && "c"    → "c"  ✅
//   "a" — truthy → идём дальше
//   "b" — truthy → идём дальше
//   "c" — последний → возвращаем "c"
//
// Ключевое правило:
//   || возвращает первый TRUTHY или последний
//   && возвращает первый FALSY или последний
//   Оба возвращают САМО ЗНАЧЕНИЕ, не true/false!
//
//
