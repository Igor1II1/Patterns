//   "0"     ← строка с символом "0" (не пустая строка!)
//   []      ← пустой массив (это объект, все объекты truthy)
//   -1      ← отрицательное число (не 0!)
//   {}      ← пустой объект (объект = truthy)
//   "false" ← строка со словом "false" (не пустая строка!)
