// Q3: TDZ (Temporal Dead Zone)
// =====================================================================
//
// Ответ Игоря: ⚠️ верно про TDZ, но ошибка про var
//
// TDZ — зона между началом блока и строкой объявления переменной.
// В этой зоне переменная СУЩЕСТВУЕТ (JS знает о ней), но НЕДОСТУПНА.
// Обращение к ней → ReferenceError.
//
// {
//   // ← TDZ для x начинается здесь (начало блока)
//   console.log(x);  // ReferenceError! x в TDZ
//   let x = 10;      // ← TDZ заканчивается здесь
//   console.log(x);  // 10 — всё ок
// }
//
// Для каких объявлений существует TDZ:
//   let   → ДА, есть TDZ
//   const → ДА, есть TDZ
//   var   → НЕТ TDZ!
//
// Что Игорь написал не так:
//   "для var будет undefined" — это правильно, но это значит что у var НЕТ TDZ.
//   var поднимается (hoisting) И сразу инициализируется как undefined.
//   let/const поднимаются, но НЕ инициализируются → попадают в TDZ.
//
// Разница:
//   console.log(a);  // undefined (var поднят и инициализирован = нет TDZ)
//   var a = 5;
//
//   console.log(b);  // ReferenceError! (let поднят, но в TDZ)
//   let b = 5;
//
