// Q2: == vs ===
// =====================================================================
//
// Ответ Игоря: ⚠️ суть верная, но без примера
//
// == (нестрогое сравнение):
//   Сначала ПРИВОДИТ операнды к одному типу, потом сравнивает.
//   5 == "5"   → true  (строка "5" приведена к числу 5)
//   0 == false → true  (false приведён к числу 0)
//   null == undefined → true  (специальное правило JS)
//
// === (строгое сравнение):
//   Сравнивает И тип, И значение. Без приведения типов.
//   5 === "5"   → false  (number !== string — разные типы, сразу false)
//   0 === false → false  (number !== boolean)
//   null === undefined → false  (null !== undefined)
//
// Пример где результат разный (которого не хватило в ответе):
//   5 == "5"    → true   (== приводит типы)
//   5 === "5"   → false  (=== не приводит)
//
// Правило: ВСЕГДА используй ===. Оператор == создаёт неочевидные ловушки.
