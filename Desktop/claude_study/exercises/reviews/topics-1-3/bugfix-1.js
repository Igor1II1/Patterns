// n=6: result.push(6)  → result = [2, 4, 6]
// return result → [2, 4, 6] ✓
//
// === Ключевое правило ===
// method(arg) — ВЫЗОВ метода (выполняет действие)
// method = arg — ПРИСВАИВАНИЕ (заменяет метод значением, ломает всё)
// push, pop, includes — все методы вызываются со СКОБКАМИ ().
