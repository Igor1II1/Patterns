// return sum → 25 (вместо 15)
//
// Нужно было:
// i=1: sum += i  →  sum = 0 + 1 = 1
// i=2: sum += i  →  sum = 1 + 2 = 3
// i=3: sum += i  →  sum = 3 + 3 = 6
// i=4: sum += i  →  sum = 6 + 4 = 10
// i=5: sum += i  →  sum = 10 + 5 = 15  ✓
