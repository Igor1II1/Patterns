// БАГ 2: Falsy-ловушка с нулём (темы 1+2)
// =====================================================================
//
// Код:      hasDiscount({ name: "Игорь", discount: 0 })
// Ожидание: "Скидка: 0%"
// Реально:  "Нет скидки"
//
// === Что происходит шаг за шагом ===
//
// 1. Вызываем hasDiscount({ name: "Игорь", discount: 0 })
//    user.discount = 0
//
// 2. if (user.discount)  →  if (0)
//    JS приводит 0 к boolean: 0 → false (0 — falsy-значение!)
//    if (false) → тело НЕ выполняется
//
// 3. Пропускаем return "Скидка: 0%" — НЕ выполнилось
//    Переходим к return "Нет скидки"
//    Результат: "Нет скидки"
//
// === Почему это баг ===
// Скидка 0% — это ВАЛИДНАЯ скидка (пользователь зарегистрирован, скидка просто нулевая).
// Но if(0) = false, и JS считает что скидки нет.
// Проблема: мы проверяем "есть ли значение", а 0 — это ЕСТЬ значение, просто равное нулю.
//
// Список falsy-значений в JS: false, 0, "", null, undefined, NaN
// Все они дают false в if(). Число 0 — в этом списке.
//
// === Ошибки Игоря ===
//
// ❌ "Реально: false"
// Неправильно. Функция возвращает СТРОКУ "Нет скидки", а не false.
// if(0) = false → тело пропущено → выполняется return "Нет скидки".
// false — это результат проверки if, а не результат функции.
//
// ❌ "Как исправить: if (0??user.discount)"
// Неправильный синтаксис. Разберём почему:
// 0 ?? user.discount  →  0 (потому что 0 — не null/undefined, ?? его не заменяет)
// if(0) → false → то же самое что было!
// Оператор ?? заменяет только null и undefined, а 0 оставляет как есть.
//
// === Правильное исправление ===
//
// Вариант 1 (лучший):
// if (user.discount !== undefined) {
//   return "Скидка: " + user.discount + "%";
// }
//
// Как работает:
// user.discount = 0  →  0 !== undefined  →  true  →  заходим в if  ✓
// user.discount = undefined  →  undefined !== undefined  →  false  →  "Нет скидки"  ✓
//
// Вариант 2:
// if (user.discount !== undefined && user.discount !== null) {
//   return "Скидка: " + user.discount + "%";
// }
//
// === Ключевое правило ===
// Никогда не используй if(value) если value может быть 0 или "".
