//
//
// =====================================================================
// БАГ 3: Условие цикла в обратную сторону (тема 3)
// =====================================================================
//
// Код:      for (let i = 10; i <= 1; i--)
// Ожидание: 10, 9, 8, ... 1
// Реально:  ничего (пустой вывод)
//
// === Что происходит шаг за шагом ===
//
// 1. let i = 10      ← начальное значение: i = 10
// 2. Проверка: i <= 1  →  10 <= 1  →  false
// 3. false → цикл НЕ ЗАПУСКАЕТСЯ. Ни одной итерации.
//
// Тело цикла console.log(i) не выполнится ни разу.
// Результат: пустой вывод (ничего).
//
// === Почему это баг ===
// Условие цикла (вторая часть for) — это проверка "продолжать ли?".
// i <= 1 означает "пока i меньше или равно 1". Но i начинается с 10!
// 10 не <= 1, поэтому цикл сразу завершается.
//
// Для обратного отсчёта нужно: "пока i больше или равно 1" → i >= 1.
//
// === Ошибки Игоря ===
//
// ❌ "Реально: 10"
// Неправильно. Цикл не выполнится НИ РАЗУ — условие false с самого начала.
