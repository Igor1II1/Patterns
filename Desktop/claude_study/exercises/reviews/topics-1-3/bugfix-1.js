// === Почему это баг ===
// n — это ПАРАМЕТР функции. Он всегда = 5 (не меняется в цикле).
// i — это СЧЁТЧИК цикла. Он меняется: 1, 2, 3, 4, 5.
// Перепутали n и i. Одна буква — совсем другой результат.
//
// === Ошибка Игоря ===
//
// Объяснение бага: ✅ правильное ("прибавляем 5 на каждой итерации")
//
// ❌ "sum += [i]"
// Неправильный синтаксис! Разберём:
//
// sum += i    ← прибавляет ЧИСЛО i к sum. При i=3: sum = sum + 3
// sum += [i]  ← прибавляет МАССИВ [i] к sum!
//                [i] — это массив из одного элемента (значение i)
//                При i=3: [3] — массив
//                sum + [3] → JS приводит [3] к строке "3"
//                sum (число) + "3" (строка) = конкатенация!
//                0 + [1] + [2] + [3] = "0123" (строка, не число!)
//
// [i] — квадратные скобки создают массив. Не путай с математическими скобками.
// В JS: (i) — группировка, [i] — массив или доступ по индексу.
//
// === Правильное исправление ===
// sum += i;  // просто i, без скобок — переменная цикла
//
// === Ключевое правило ===
// Переменные пишутся БЕЗ скобок: i, n, sum, count.
// [i] — это массив из одного элемента. Совсем другое!
// Если нужна группировка — круглые скобки: (i + 1), не [i + 1].
//
//
// =====================================================================
// БАГ 8: ?? маскирует разницу между "не задан" и "задан как 0" (темы 1+2+3)
// =====================================================================
//
// Код:      const limit = minPrice ?? 0;
// Ожидание: "не задан" → показать все, "задан как 0" → показать от 0
// Реально:  оба случая дают одно и то же (limit = 0)
//
// === Ответ Игоря: ❌ не нашёл баг ("не вижу баг вроде все работает") ===
//
// Это действительно тонкий баг. Давай разберём подробно.
//
// === Как работает ?? (nullish coalescing) ===
//
// a ?? b означает: "если a — null или undefined, верни b. Иначе верни a."
//
// undefined ?? 0  →  0    (undefined заменено на 0)
// null ?? 0       →  0    (null заменено на 0)
// 5 ?? 0          →  5    (5 — не null/undefined, оставляем)
// 0 ?? 0          →  0    (0 — не null/undefined, оставляем!)
//
// === Где проблема ===
//
// Вызов 1: filterByPrice(products, undefined)  — "фильтр НЕ задан"
//   const limit = undefined ?? 0  →  limit = 0
//   Фильтруем: p.price >= 0 → все товары проходят (цены не бывают < 0)
//   Результат: ["Мышь", "Монитор", "Кабель"] ← правильно
//
// Вызов 2: filterByPrice(products, 0)  — "фильтр задан: от 0 руб"
//   const limit = 0 ?? 0  →  limit = 0
//   Фильтруем: p.price >= 0 → все товары проходят
//   Результат: ["Мышь", "Монитор", "Кабель"] ← тоже правильно... пока.
//
// Вызов 1 и вызов 2 дают ОДИНАКОВЫЙ результат. В данном случае это совпадение:
// "не задан" и "от 0" оба дают limit=0, и оба корректны.
//
// === Когда это СЛОМАЕТСЯ ===
//
// Представь, что дефолт не 0, а 100:
//   const limit = minPrice ?? 100;  // "если не задан — показать от 100"
//
//   filterByPrice(products, undefined)  →  limit = 100  ← правильно (дефолт 100)
//   filterByPrice(products, 0)          →  limit = 0    ← правильно (0 — не null/undefined)
//
// Тут ?? работает нормально! Но проблема АРХИТЕКТУРНАЯ:
//
// Мы СМЕШИВАЕМ два разных смысла в одно число:
// - "фильтр не задан" (показать ВСЕ товары) — это НЕ число, это отсутствие фильтра
// - "фильтр = 0" (показать от 0 рублей) — это число 0
//
// С ?? оба случая превращаются в число, и мы теряем информацию
// "был ли фильтр задан вообще".
//
// === Правильный подход (как в practice-6) ===
//
// Не превращать undefined в число. Вместо этого — проверять "задан ли фильтр":
//
// function filterByPrice(products, minPrice) {
//   let result = [];
//   for (const p of products) {
//     if (minPrice !== undefined && p.price < minPrice) {
//       continue;  // фильтр задан И цена ниже → пропускаем
//     }
//     result.push(p.name);
//   }
//   return result;
// }
//
// Как работает:
//
// filterByPrice(products, undefined):
//   minPrice !== undefined → false → false && ... → false → continue НЕ срабатывает
//   → ВСЕ товары попадают в result ✓
//
// filterByPrice(products, 0):
//   minPrice !== undefined → true → проверяем p.price < 0
//   Мышь: 500 < 0 → false → попадает ✓
//   Кабель: 0 < 0 → false → попадает ✓
//
// filterByPrice(products, 1000):
//   minPrice !== undefined → true → проверяем p.price < 1000
//   Мышь: 500 < 1000 → true → continue (пропущена)
//   Монитор: 25000 < 1000 → false → попадает ✓
//   Кабель: 0 < 1000 → true → continue (пропущен)
//
// === Ключевое правило ===
// ?? хорош когда нужно ЗАМЕНИТЬ undefined на конкретное значение.
// Но если undefined означает "условие не применяется" (а не "используй дефолт") —
// лучше проверять !== undefined && условие.
// Это паттерн "необязательного фильтра" из practice-6.
//
//
// =====================================================================
// ИТОГОВАЯ ТАБЛИЦА РЕЗУЛЬТАТОВ
// =====================================================================
//
// | Баг | Суть бага            | Понял причину | Правильный fix | Итог  |
// |-----|----------------------|:---:|:---:|:-----:|
// | 1   | строка + число       | ✅  | ✅  | ✅    |
// | 2   | falsy 0 в if()       | ✅  | ❌  | ⚠️    |
// | 3   | условие цикла        | ⚠️  | ⚠️  | ⚠️    |
// | 4   | continue наоборот    | ✅  | ✅  | ✅    |
// | 5   | return vs console    | ✅  | ✅  | ✅    |
// | 6   | push vs присваивание | ✅  | ❌  | ⚠️    |
// | 7   | n вместо i           | ✅  | ❌  | ⚠️    |
// | 8   | ?? маскирует undefined | ❌ | —   | ❌    |
//
// Результат: 3 полностью правильно, 4 частично (суть понял, синтаксис неточный), 1 не нашёл.
