// === Паттерн ошибок Игоря ===
//
// Главная проблема: СИНТАКСИС записи исправлений.
// Суть багов Игорь понимает хорошо (7 из 8), но при записи кода путает:
//
// 1. result.push(n) vs result.push = n
//    → Скобки () = вызов функции, = после метода = присваивание (ломает метод)
//
// 2. sum += i vs sum += [i]
//    → Переменная без скобок. [i] создаёт массив, а не группирует выражение
//
// 3. !== undefined vs ??
//    → ?? заменяет значение. !== undefined проверяет наличие.
//      Для фильтров лучше !== undefined.
//
// 4. >= vs > (включение границы)
//    → >= включает число (1 >= 1 = true), > не включает (1 > 1 = false)
//
// Это нормально на данном этапе — синтаксис закрепляется практикой.
// С каждым заданием ошибок будет меньше.
