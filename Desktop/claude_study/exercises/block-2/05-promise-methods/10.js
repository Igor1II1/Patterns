// Вспомогательная функция (не меняй):
function пингСервер(имя, мс, упадёт = false) {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      if (упадёт) {
        reject(new Error(имя + " не отвечает"));
      } else {
        resolve({ сервер: имя, время: мс + "мс" });
      }
    }, мс);
  });
}

