// Debounce — откладывает вызов функции до тех пор, пока не пройдёт
// указанное время с последнего вызова. Если вызовы идут подряд —
// таймер перезапускается.
//
// Напиши функцию `debounce(fn, delayMs)`:
// - Возвращает новую функцию
// - При каждом вызове сбрасывает таймер (clearTimeout)
// - fn вызывается только когда пользователь "успокоился" (перестал вызывать)
//
// Это ключевой паттерн для:
// - Поиска по мере ввода (не отправлять запрос на каждую букву)
// - Обработки resize/scroll событий
